package Modelos;

/**
 *
 * @author sandracano
 */
public class Regiones {
    
    private String nombre;
    private int id;

    public Regiones(String nombre, int id) {
        this.nombre = nombre;
        this.id = id;
    }

    public Regiones() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
    
}
